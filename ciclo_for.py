#Coleccion de datos de Ejemplo:

#Lista: conjunto de datos simples:
#      se encierra entre brackets 
#      se separan por comas
#      tama de la lista: numero de elementos 
lista = ["Apple",
         "Orange",
         "Strawberry",
         "Grape",
         "Banana",]

#iteremos la lista con un ciclo for
#variable de almacenamiento
#     singular del tipo de objetos
#    que contiene la lista
for fruta in lista: 
    print (fruta)   
    
#Acceder a una fruta en particula
print("fruta individual:" ,lista [1])